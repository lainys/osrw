package ru.miet.orgact;

public class Other {

    private String name;
    private String link;

    public Other() {
        name = "";
        link = "";

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
